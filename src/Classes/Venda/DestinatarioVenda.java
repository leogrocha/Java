package Classes.Venda;

public class DestinatarioVenda {
	private String cpf_cnpj;
	private String nome_razao;
	private int idcidade;
	private String cidade;
	private String estado;
	private String cep;
	private String logradouro;
	private int numero;
	private String bairro;
	private String telefone;
	private String iscestadual;
	
	public String getCpf_cnpj() {
		return cpf_cnpj;
	}
	public void setCpf_cnpj(String cpf_cnpj) {
		this.cpf_cnpj = cpf_cnpj;
	}
	public String getNome_razao() {
		return nome_razao;
	}
	public void setNome_razao(String nome_razao) {
		this.nome_razao = nome_razao;
	}
	public int getIdcidade() {
		return idcidade;
	}
	public void setIdcidade(int idcidade) {
		this.idcidade = idcidade;
	}
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getCep() {
		return cep;
	}
	public void setCep(String cep) {
		this.cep = cep;
	}
	public String getLogradouro() {
		return logradouro;
	}
	public void setLogradouro(String logradouro) {
		this.logradouro = logradouro;
	}
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
	public String getBairro() {
		return bairro;
	}
	public void setBairro(String bairro) {
		this.bairro = bairro;
	}
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}
	public String getIscestadual() {
		return iscestadual;
	}
	public void setIscestadual(String iscestadual) {
		this.iscestadual = iscestadual;
	}
	
	
}
